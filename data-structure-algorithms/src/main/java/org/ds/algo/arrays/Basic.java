package org.ds.algo.arrays;

import static java.lang.System.out;

public class Basic {

    public static void main(String[] args) {

        int[] aa = new int[6];

        aa[0] = 54;
        aa[1] = -2;
        aa[2] = 78;
        aa[3] = 153;
        aa[4] = -49;
        aa[5] = 540;

        for (int i = 0; i < aa.length; i++) {
            out.println(aa[i]);
        }
    }
}
